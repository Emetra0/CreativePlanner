export function resolveMessages(language: string) {
  const sourceControlledMessages = getSourceControlledMessages(language);
  const exact = TRANSLATIONS[language];
  if (exact) return { ...EN_MESSAGES, ...exact, ...sourceControlledMessages };

  const baseLanguage = language.split('-')[0];
  const base = TRANSLATIONS[baseLanguage];
  if (base) return { ...EN_MESSAGES, ...base, ...sourceControlledMessages };

  return { ...EN_MESSAGES, ...sourceControlledMessages };
}

export function getLegacyMessagesForLanguage(language: string) {
  const exact = TRANSLATIONS[language];
  if (exact) return exact;

  const baseLanguage = language.split('-')[0];
  return TRANSLATIONS[baseLanguage] ?? {};
}

function interpolateParams(value: string, params?: Record<string, string>) {
  if (!params) return value;

  let nextValue = value;
  for (const [paramKey, paramValue] of Object.entries(params)) {
    nextValue = nextValue.replace(new RegExp(`{{${paramKey}}}`, 'g'), paramValue);
  }

  return nextValue;
}

export function translateAppText(language: string, originalText: string, params?: Record<string, string>) {
  const translated = language.startsWith('en')
    ? originalText
    : translateStaticAppText(language, originalText) ?? originalText;

  return interpolateParams(translated, params);
}

export function useAppTranslation() {
  const appLanguage = useSettingsStore((state) => state.appLanguage);
  const resolvedLanguage = useMemo(() => resolveAppLanguage(appLanguage), [appLanguage]);
  const { t: i18nT, i18n } = useTranslation(undefined, { useSuspense: false });

  const t = (key: AppTranslationKey, params?: Record<string, string>) => {
    return i18nT(key, {
      ...params,
      defaultValue: EN_MESSAGES[key],
    });
  };

  const text = (value: string, params?: Record<string, string>) => translateAppText(resolvedLanguage, value, params);

  return {
    language: resolvedLanguage,
    t,
    text,
    i18n,
  };
}